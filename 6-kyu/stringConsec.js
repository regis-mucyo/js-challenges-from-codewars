const longestConsec = (strarr, k) => {
  let array = [];
  if (k <= 0 || k > strarr.length) return "";
  for (
    let i = 0;
    i < strarr.length - k;
    k === 1 ? (i += k) : k === 2 ? (i += k - 1) : (i += k)
  ) {
    array.push(strarr.slice(i, i + k));
  }
  let newArray = array.map((e) => e.join(""));
  let num = newArray.map((e) => e.length);
  let index = num.indexOf(Math.max(...num));
  return newArray[index];
};
